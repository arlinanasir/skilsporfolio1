# step 1: Count up from 0 to 20 in increments of 1
print("Counting up from 0 to 20:")
for i in range(0, 21):
    print(i, end=" ")
print("\n")
# step 2: Count down from 40 to 0 in decrements of 1
print("Counting down from 40 to 0:")
for i in range(40, -1, -1):
    print(i, end=" ")
print("\n")
# step 3: Count up from 10 to 50 in increments of 1
print("Counting up from 10 to 50:")
for i in range(10, 51):
    print(i, end=" ")
print("\n")
# step 4: Count down from 70 to 10 in decrements of 2
print("Counting down from 70 to 10 in decrements of 2:")
for i in range(70, 9, -2):
    print(i, end=" ")
print("\n")
# step 5: Count up from 100 to 151 in increments of 5
print("Counting up from 100 to 151 in increments of 5:")
for i in range(100, 151, 5):
    print(i, end=" ")
print()
